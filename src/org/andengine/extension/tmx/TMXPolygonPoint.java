package org.andengine.extension.tmx;

public class TMXPolygonPoint {
	// ===========================================================
	// Constants
	// ===========================================================

	// ===========================================================
	// Fields
	// ===========================================================
	
	private float mX;
	private float mY;

	// ===========================================================
	// Constructors
	// ===========================================================

	public TMXPolygonPoint(final String pPoint) {
		String[] coords = pPoint.split(",");
		this.mX = Float.parseFloat(coords[0]);
		this.mY = Float.parseFloat(coords[1]);
	}

	// ===========================================================
	// Getter & Setter
	// ===========================================================
	
	public float getX() {
		return this.mX;
	}
	
	public float getY() {
		return this.mY;
	}

	// ===========================================================
	// Methods for/from SuperClass/Interfaces
	// ===========================================================

	// ===========================================================
	// Methods
	// ===========================================================

	// ===========================================================
	// Inner and Anonymous Classes
	// ===========================================================
}
